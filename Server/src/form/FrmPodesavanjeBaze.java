    private void ucitajPodesavanja() {
        try {
            Properties properties = new Properties();

            InputStream input = new FileInputStream(PUTANJA);
            properties.load(input);
            input.close();

            txtUrl.setText(properties.getProperty("url", ""));
            txtUsername.setText(properties.getProperty("username", ""));
            txtPassword.setText(properties.getProperty("password", ""));

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Greška pri učitavanju podešavanja baze: " + ex.getMessage());
        }
    }

