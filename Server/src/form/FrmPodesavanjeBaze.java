    private void sacuvajPodesavanja() {
        try {
            Properties properties = new Properties();

            properties.setProperty("url", txtUrl.getText().trim());
            properties.setProperty("username", txtUsername.getText().trim());
            properties.setProperty("password", txtPassword.getText().trim());

            OutputStream output = new FileOutputStream(PUTANJA);
            properties.store(output, null);
            output.close();

            JOptionPane.showMessageDialog(this,
                    "Podešavanja baze su sačuvana.\nPromene će biti primenjene nakon ponovnog pokretanja servera.");
            dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Greška pri čuvanju podešavanja baze: " + ex.getMessage());
        }
    }

