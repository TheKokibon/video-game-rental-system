    private void ucitajClanoveUCombo() {
        try {
            transfer.Response res = communication.Communication.getInstance().send(
                    new transfer.Request(transfer.enums.RequestType.VRATI_LISTU_CLANOVA, null)
            );

            if (!res.isSuccessful()) {
                throw res.getException();
            }

            java.util.List<domain.Clan> list = (java.util.List<domain.Clan>) res.getResult();

            cmbClanovi.removeAllItems();
            for (domain.Clan c : list) {
                cmbClanovi.addItem(c);
            }

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, e.getMessage());
            e.printStackTrace();
        }
    }

    private void izracunajNoviIstek() throws Exception {
        if (aktivna == null) {
            return;
        }

        int trajanje = aktivna.getPaket() != null ? aktivna.getPaket().getTrajanjeDana() : 0;
        if (trajanje <= 0) {
            // Ako nema trajanjeDana, ne možemo računati na klijentu
            // (u tom slučaju ili dodaj JOIN na serveru ili u UI traži unos novog datuma)
            return;
        }

        java.util.Calendar cal = java.util.Calendar.getInstance();
        cal.setTime(aktivna.getDatumIsteka());
        cal.add(java.util.Calendar.DAY_OF_MONTH, trajanje);

        java.util.Date novi = cal.getTime();
        txtNoviDatumIsteka.setText(SDF.format(novi));
    }
