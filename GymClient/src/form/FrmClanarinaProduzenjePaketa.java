    private void btnUcitajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUcitajActionPerformed
        try {
            domain.Clan c = (domain.Clan) cmbClanovi.getSelectedItem();
            if (c == null) {
                javax.swing.JOptionPane.showMessageDialog(this, "Izaberi člana.");
                return;
            }

            transfer.Response res = communication.Communication.getInstance().send(
                    new transfer.Request(
                            transfer.enums.RequestType.PRONADJI_AKTIVNU_CLANARINU_ZA_CLANA,
                            Integer.valueOf(c.getClanID())
                    )
            );

            if (!res.isSuccessful()) {
                throw res.getException();
            }

            aktivna = (domain.Clanarina) res.getResult();

            if (aktivna == null) {
                txtTrenutniDatumIsteka.setText("");
                txtNoviDatumIsteka.setText("");
                javax.swing.JOptionPane.showMessageDialog(this, "Član nema aktivnu članarinu.");
                return;
            }

            // Prikaz trenutnog isteka
            txtTrenutniDatumIsteka.setText(SDF.format(aktivna.getDatumIsteka()));
            txtNoviDatumIsteka.setText("");

            // Ako server vraća i trajanjeDana u aktivna.getPaket().getTrajanjeDana(),
            // možemo odmah izračunati novi datum:
            izracunajNoviIstek();

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, e.getMessage(), "Greška", javax.swing.JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnUcitajActionPerformed

    private void btnProduziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProduziActionPerformed
        try {
            domain.Clan c = (domain.Clan) cmbClanovi.getSelectedItem();
            if (c == null) {
                javax.swing.JOptionPane.showMessageDialog(this, "Izaberi člana.");
                return;
            }

            if (aktivna == null) {
                javax.swing.JOptionPane.showMessageDialog(this, "Prvo učitaj aktivnu članarinu.");
                return;
            }

            String noviText = txtNoviDatumIsteka.getText().trim();
            if (noviText.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Novi datum isteka nije izračunat.");
                return;
            }

            java.util.Date noviUtil;
            try {
                noviUtil = SDF.parse(noviText);
            } catch (java.text.ParseException ex) {
                javax.swing.JOptionPane.showMessageDialog(this, "Novi datum mora biti u formatu dd.MM.yyyy");
                return;
            }

            int confirm = javax.swing.JOptionPane.showConfirmDialog(
                    this,
                    "Produži članarinu do: " + noviText + " ?",
                    "Potvrda produženja",
                    javax.swing.JOptionPane.YES_NO_OPTION
            );
            if (confirm != javax.swing.JOptionPane.YES_OPTION) {
                return;
            }

            transfer.ProduziPaketRequest pr = new transfer.ProduziPaketRequest(
                    c.getClanID(),
                    new java.sql.Date(noviUtil.getTime())
            );

            transfer.Response res = communication.Communication.getInstance().send(
                    new transfer.Request(transfer.enums.RequestType.PRODUZI_PAKET_CLANU, pr)
            );

            if (!res.isSuccessful()) {
                throw res.getException();
            }

            javax.swing.JOptionPane.showMessageDialog(this, "Članarina je produžena.");

            // osveži prikaz
            btnUcitajActionPerformed(null);

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, e.getMessage(), "Greška", javax.swing.JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }    }//GEN-LAST:event_btnProduziActionPerformed

    private void btnIzracunajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzracunajActionPerformed
        try {
            if (aktivna == null) {
                javax.swing.JOptionPane.showMessageDialog(this, "Prvo učitaj aktivnu članarinu.");
                return;
            }
            izracunajNoviIstek();
            if (txtNoviDatumIsteka.getText().trim().isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Nije moguće izračunati novi istek (nema trajanja paketa).");
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, e.getMessage());
            e.printStackTrace();
        }    }//GEN-LAST:event_btnIzracunajActionPerformed

