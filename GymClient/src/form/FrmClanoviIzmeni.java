    private void ucitajClanoveUCombo() {
        try {
            transfer.Request req = new transfer.Request(
                    transfer.enums.RequestType.VRATI_LISTU_CLANOVA, null);

            transfer.Response res = communication.Communication.getInstance().send(req);

            if (res == null || !res.isSuccessful()) {
                if (res != null && res.getException() != null) {
                    res.getException().printStackTrace();
                }
                javax.swing.JOptionPane.showMessageDialog(
                        this,
                        "Sistem ne može da pronađe članove po zadatoj vrednosti.",
                        "Greška",
                        javax.swing.JOptionPane.ERROR_MESSAGE
                );
                return;
            }

            java.util.List<domain.Clan> list = (java.util.List<domain.Clan>) res.getResult();

            cmbClan.removeAllItems();
            for (domain.Clan c : list) {
                cmbClan.addItem(c);
            }

        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Sistem ne može da pronađe članove po zadatoj vrednosti.",
                    "Greška",
                    javax.swing.JOptionPane.ERROR_MESSAGE
            );
        }
    }
