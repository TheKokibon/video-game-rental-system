    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        try {
            domain.Clan selected = (domain.Clan) cmbClan.getSelectedItem();
            if (selected == null) {
                javax.swing.JOptionPane.showMessageDialog(this, "Izaberi člana.");
                return;
            }

            // napravimo objekat za update
            domain.Clan c = new domain.Clan();
            c.setClanID(selected.getClanID());
            c.setIme(txtIme.getText().trim());
            c.setPrezime(txtPrezime.getText().trim());
            c.setEmail(txtEmail.getText().trim());
            c.setTelefon(txtTelefon.getText().trim());
            c.setAktivan(chckboxAktivan.isSelected());

            // admin prosleđen kroz konstruktor (kako si hteo)
            domain.Administrator a = new domain.Administrator();
            a.setAdministratorID(admin.getAdministratorID());
            c.setAdministrator(a);

            transfer.Request req = new transfer.Request(
                    transfer.enums.RequestType.IZMENI_CLANA, c);

            transfer.Response res = communication.Communication.getInstance().send(req);

            if (!res.isSuccessful()) {
                throw res.getException();
            }

            javax.swing.JOptionPane.showMessageDialog(this, "Član je izmenjen.");

            // (opciono) osveži combo da povuče nove podatke iz baze:
            ucitajClanoveUCombo();

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, e.getMessage(), "Greška", javax.swing.JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed

