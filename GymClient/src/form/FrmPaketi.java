    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
       try {
            String q = txtPretraga.getText().trim();

            transfer.Request req = new transfer.Request(
                    transfer.enums.RequestType.PRONADJI_PAKETE, q
            );

            transfer.Response res = communication.Communication.getInstance().send(req);

            if (!res.isSuccessful()) {
                throw res.getException();
            }

            java.util.List<domain.Paket> list
                    = (java.util.List<domain.Paket>) res.getResult();

            model.PaketTableModel m = (model.PaketTableModel) tblPaketi.getModel();
            m.setData(list);

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, e.getMessage());
            e.printStackTrace();
        }
       
    }//GEN-LAST:event_btnPretraziActionPerformed

   

