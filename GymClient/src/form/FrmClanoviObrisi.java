    private void ucitajClanoveUCombo() {
        try {
            transfer.Request req = new transfer.Request(
                    transfer.enums.RequestType.VRATI_LISTU_CLANOVA, null);

            transfer.Response res = communication.Communication.getInstance().send(req);

            if (!res.isSuccessful()) {
                throw res.getException();
            }

            java.util.List<domain.Clan> list
                    = (java.util.List<domain.Clan>) res.getResult();

            cmbClan.removeAllItems();
            for (domain.Clan c : list) {
                cmbClan.addItem(c);
            }

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, e.getMessage());
            e.printStackTrace();
        }
    }

