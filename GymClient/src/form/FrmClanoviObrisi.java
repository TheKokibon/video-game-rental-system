    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        try {
            domain.Clan c = (domain.Clan) cmbClan.getSelectedItem();
            if (c == null) {
                javax.swing.JOptionPane.showMessageDialog(this, "Izaberi člana.");
                return;
            }

            int confirm = javax.swing.JOptionPane.showConfirmDialog(
                    this,
                    "Da li si siguran da želiš da obrišeš člana:\n" + c.getIme() + " " + c.getPrezime() + "?",
                    "Potvrda brisanja",
                    javax.swing.JOptionPane.YES_NO_OPTION
            );

            if (confirm != javax.swing.JOptionPane.YES_OPTION) {
                return;
            }

            transfer.Request req = new transfer.Request(
                    transfer.enums.RequestType.OBRISI_CLANA,
                    Integer.valueOf(c.getClanID())
            );

            transfer.Response res = communication.Communication.getInstance().send(req);

            if (!res.isSuccessful()) {
                throw res.getException();
            }

            javax.swing.JOptionPane.showMessageDialog(this, "Član je obrisan.");

            // osveži combo
            ucitajClanoveUCombo();

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, e.getMessage(), "Greška", javax.swing.JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnObrisiActionPerformed


