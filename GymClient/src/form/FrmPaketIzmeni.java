    private void ucitajPaketeUCombo() {
        try {
            transfer.Request req = new transfer.Request(
                    transfer.enums.RequestType.VRATI_LISTU_PAKETA, null);

            transfer.Response res = communication.Communication.getInstance().send(req);

            if (!res.isSuccessful()) {
                throw res.getException();
            }

            java.util.List<domain.Paket> list
                    = (java.util.List<domain.Paket>) res.getResult();

            cmbPaketi.removeAllItems();
            for (domain.Paket p : list) {
                cmbPaketi.addItem(p);
            }

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, e.getMessage());
            e.printStackTrace();
        }

    }

    private void popuniPoljaIzSelektovanog() {
        domain.Paket p = (domain.Paket) cmbPaketi.getSelectedItem();
        if (p == null) {
            return;
        }

        txtNazivPaketa.setText(p.getNaziv());
        txtTrajanjeDana.setText(Integer.toString(p.getTrajanjeDana()));
        txtCena.setText(Double.toString(p.getCena()));
        txtBrojDolazaka.setText(Integer.toString(p.getBrojDozvoljenihDolazaka()));

    }
