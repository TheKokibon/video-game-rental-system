    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        try {
            domain.Paket selected = (domain.Paket) cmbPaketi.getSelectedItem();
            if (selected == null) {
                javax.swing.JOptionPane.showMessageDialog(this, "Izaberi paket.");
                return;
            }

            String naziv = txtNazivPaketa.getText().trim();
            String trajanjeTekst = txtTrajanjeDana.getText().trim();
            String cenaTekst = txtCena.getText().trim();
            String brojDolazakaTekst = txtBrojDolazaka.getText().trim();

            if (naziv.isEmpty() || trajanjeTekst.isEmpty() || cenaTekst.isEmpty() || brojDolazakaTekst.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Sva polja su obavezna.");
                return;
            }

            int trajanjeDana;
            double cena;
            int brojDolazaka;

            try {
                trajanjeDana = Integer.parseInt(trajanjeTekst);
                cena = Double.parseDouble(cenaTekst);
                brojDolazaka = Integer.parseInt(brojDolazakaTekst);
            } catch (NumberFormatException nfe) {
                javax.swing.JOptionPane.showMessageDialog(this, "Trajanje, cena i broj dolazaka moraju biti brojevi.");
                return;
            }

            if (trajanjeDana <= 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Trajanje mora biti > 0.");
                return;
            }
            if (cena <= 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Cena mora biti > 0.");
                return;
            }
            if (brojDolazaka <= 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Broj dolazaka mora biti > 0.");
                return;
            }

            // objekat za update
            domain.Paket p = new domain.Paket();
            p.setPaketID(selected.getPaketID());
            p.setNaziv(naziv);
            p.setTrajanjeDana(trajanjeDana);
            p.setCena(cena);
            p.setBrojDozvoljenihDolazaka(brojDolazaka);

            // admin prosleđen kroz konstruktor
            domain.Administrator a = new domain.Administrator();
            a.setAdministratorID(admin.getAdministratorID());
            p.setAdministrator(a);

            transfer.Request req = new transfer.Request(
                    transfer.enums.RequestType.IZMENI_PAKET, p
            );

            transfer.Response res = communication.Communication.getInstance().send(req);

            if (!res.isSuccessful()) {
                throw res.getException();
            }

            javax.swing.JOptionPane.showMessageDialog(this, "Paket je izmenjen.");

            // osveži combo da povuče nove podatke iz baze
            ucitajPaketeUCombo();

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, e.getMessage(), "Greška", javax.swing.JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed

