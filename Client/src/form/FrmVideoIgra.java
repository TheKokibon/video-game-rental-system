    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        try {
            String kriterijum = txtKriterijum.getText().trim();

            if (kriterijum.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Unesite kriterijum za pretragu.");
                return;
            }

            VideoIgra vi = new VideoIgra();
            vi.setNaziv(kriterijum);

            List<VideoIgra> lista = Communication.getInstance().pretraziVideoIgre(vi);

            ModelTabeleVideoIgre model = (ModelTabeleVideoIgre) tblVideoIgra.getModel();
            model.setLista(lista);

            if (lista.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Sistem ne može da nađe video igre po zadatoj vrednosti.");
            } else {
                JOptionPane.showMessageDialog(this, "Sistem je našao video igre po zadatoj vrednosti.");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Greška pri pretrazi: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnPretraziActionPerformed

    private void btnUcitajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUcitajActionPerformed
        try {
            Response response = Communication.getInstance().ucitajVideoIgre();

            if (response.getStatus() == ResponseStatus.ERROR) {
                throw response.getException();
            }

            List<VideoIgra> lista = (List<VideoIgra>) response.getPodatak();

            ModelTabeleVideoIgre model = (ModelTabeleVideoIgre) tblVideoIgra.getModel();
            model.setLista(lista);

            JOptionPane.showMessageDialog(this, "Sistem je učitao video igre.");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da učita video igre.\n" + ex.getMessage());
        }
    }//GEN-LAST:event_btnUcitajActionPerformed

