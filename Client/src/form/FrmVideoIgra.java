    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        try {
            int selectedRow = tblVideoIgra.getSelectedRow();

            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(this, "Sistem ne može da obriše video igru. Niste izabrali video igru.");
                return;
            }

            ModelTabeleVideoIgre model = (ModelTabeleVideoIgre) tblVideoIgra.getModel();
            VideoIgra videoIgra = model.vratiVideoIgru(selectedRow);

            int potvrda = JOptionPane.showConfirmDialog(
                    this,
                    "Da li ste sigurni da želite da obrišete video igru?",
                    "Potvrda brisanja",
                    JOptionPane.YES_NO_OPTION
            );

            if (potvrda == JOptionPane.YES_OPTION) {
                Response response = Communication.getInstance().obrisiVideoIgru(videoIgra);

                if (response.getStatus() == ResponseStatus.ERROR) {
                    throw response.getException();
                }

                model.obrisiVideoIgru(selectedRow);
                JOptionPane.showMessageDialog(this, "Sistem je obrisao video igru.");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da obriše video igru.\n" + ex.getMessage());
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

