    private void ucitajSveOsobe() {
        try {
            Response response = Communication.getInstance().ucitajOsobe();

            if (response.getStatus() == ResponseStatus.SUCCESS) {
                java.util.List<Osoba> lista = (java.util.List<Osoba>) response.getPodatak();
                model.setLista(lista);
                JOptionPane.showMessageDialog(this, "Sistem je ucitao osobe.");
            } else {
                JOptionPane.showMessageDialog(this,
                        "Sistem ne moze da ucita osobe.\n"
                        + response.getException().getMessage());
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Sistem ne moze da ucita osobe.\n" + e.getMessage());
        }
    }

