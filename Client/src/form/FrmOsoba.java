    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
           try {

        String kriterijum = txtKriterijum.getText().trim();
        KategorijaOsobe kategorija = (KategorijaOsobe) cmbKategorijaOsobe.getSelectedItem();

        Osoba osoba = new Osoba();

        if (!kriterijum.isEmpty()) {
            osoba.setIme(kriterijum);
        }

        if (kategorija != null && kategorija.getIdKategorijaOsobe() != null) {
            osoba.setKategorijaOsobe(kategorija);
        }

        List<Osoba> lista = Communication.getInstance().pretraziOsobe(osoba);

        if(lista != null){
            JOptionPane.showMessageDialog(this, "Sistem je našao osobe po zadatim kriterijumima.");
        }
        ModelTabeleOsobe model = new ModelTabeleOsobe(lista);
        tblOsobe.setModel(model);

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, ex.getMessage());
    }
    }//GEN-LAST:event_btnPretraziActionPerformed

    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed

        try {
            int selectedRow = tblOsobe.getSelectedRow();

            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(this, "Sistem ne može da učita osobu. Niste izabrali osobu.");
                return;
            }

            ModelTabeleOsobe model = (ModelTabeleOsobe) tblOsobe.getModel();
            Osoba osoba = model.getOsoba(selectedRow);

            JOptionPane.showMessageDialog(this, "Sistem je učitao osobu.");

            IzmeniOsobu fio = new IzmeniOsobu(osoba);
            fio.setLocationRelativeTo(this);
            fio.setVisible(true);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Greška pri otvaranju forme za izmenu: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        try {
            int selectedRow = tblOsobe.getSelectedRow();

            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(this, "Sistem ne može da obriše osobu. Niste izabrali osobu.");
                return;
            }

            ModelTabeleOsobe model = (ModelTabeleOsobe) tblOsobe.getModel();
            Osoba osoba = model.getOsoba(selectedRow);

            int potvrda = JOptionPane.showConfirmDialog(
                    this,
                    "Da li ste sigurni da želite da obrišete osobu?",
                    "Potvrda brisanja",
                    JOptionPane.YES_NO_OPTION
            );

            if (potvrda == JOptionPane.YES_OPTION) {

                Response response = Communication.getInstance().obrisiOsobu(osoba);

                if (response.getStatus() == ResponseStatus.SUCCESS) {

                    model.obrisiOsobu(selectedRow);

                    JOptionPane.showMessageDialog(this, "Sistem je obrisao osobu.");

                } else {
                    throw response.getException();
                }
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da obriše osobu.\n" + ex.getMessage());
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

