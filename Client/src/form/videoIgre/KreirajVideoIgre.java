    private void ocistiPolja() {
        txtNaziv.setText("");
        txtZanr.setText("");
        txtPlatforma.setText("");
        txtCena.setText("");
        txtOpis.setText("");
    }

    private void validirajUnos() throws Exception {
        if (txtNaziv.getText().trim().isEmpty()) {
            throw new Exception("Naziv je obavezan.");
        }

        if (txtZanr.getText().trim().isEmpty()) {
            throw new Exception("Žanr je obavezan.");
        }

        if (txtPlatforma.getText().trim().isEmpty()) {
            throw new Exception("Platforma je obavezna.");
        }

        if (txtCena.getText().trim().isEmpty()) {
            throw new Exception("Cena je obavezna.");
        }

        try {
            double cena = Double.parseDouble(txtCena.getText().trim());
            if (cena <= 0) {
                throw new Exception("Cena mora biti veća od 0.");
            }
        } catch (NumberFormatException e) {
            throw new Exception("Cena mora biti broj.");
        }
    }
