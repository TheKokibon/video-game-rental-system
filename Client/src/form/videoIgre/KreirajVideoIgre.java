    private void btnKreirajVideoIgreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKreirajVideoIgreActionPerformed
        try {
            ModelTabeleVideoIgre model = (ModelTabeleVideoIgre) tblVideoIgre.getModel();

            if (model.getLista().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Sistem ne može da zapamti video igru.");
                return;
            }

            for (VideoIgra vi : model.getLista()) {
                Response response = Communication.getInstance().kreirajVideoIgru(vi);

                if (response.getStatus() == ResponseStatus.ERROR) {
                    throw response.getException();
                }
            }

            JOptionPane.showMessageDialog(this, "Sistem je zapamtio video igre.");

            model.setLista(new java.util.ArrayList<>());
            ocistiPolja();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da zapamti video igre.\n" + ex.getMessage());
        }
    }//GEN-LAST:event_btnKreirajVideoIgreActionPerformed

    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        try {
            validirajUnos();

            String naziv = txtNaziv.getText().trim();
            String zanr = txtZanr.getText().trim();
            String platforma = txtPlatforma.getText().trim();
            double cena = Double.parseDouble(txtCena.getText().trim());
            String opis = txtOpis.getText().trim();

            VideoIgra vi = new VideoIgra(null, naziv, zanr, platforma, cena, opis);

            ModelTabeleVideoIgre model = (ModelTabeleVideoIgre) tblVideoIgre.getModel();
            model.dodajVideoIgru(vi);

            JOptionPane.showMessageDialog(this, "Igra je dodata u tabelu.");

            ocistiPolja();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }    }//GEN-LAST:event_btnDodajActionPerformed


