    private void btnPretraziIznajmljivanjeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziIznajmljivanjeActionPerformed
        try {
            Iznajmljivanje kriterijum = new Iznajmljivanje();

            String txtId = txtIdIznajmljivanje.getText().trim();
            if (!txtId.isEmpty()) {
                kriterijum.setIdIznajmljivanje(Long.parseLong(txtId));
            }

            Zaposleni z = (Zaposleni) cmbZaposleni.getSelectedItem();
            if (z != null && z.getIdZaposleni() != null) {
                kriterijum.setZaposleni(z);
            }

            Osoba o = (Osoba) cmbOsoba.getSelectedItem();
            if (o != null && o.getIdOsoba() != null) {
                kriterijum.setOsoba(o);
            }

            List<Iznajmljivanje> lista = Communication.getInstance().pretraziIznajmljivanja(kriterijum);

            VideoIgra vi = (VideoIgra) cmbVideoIgra.getSelectedItem();

            if (vi != null && vi.getIdVideoIgra() != null) {
                List<Iznajmljivanje> filtrirana = new java.util.ArrayList<>();

                for (Iznajmljivanje i : lista) {
                    List<StavkaIznajmljivanja> stavke
                            = Communication.getInstance().ucitajStavkeIznajmljivanja(i);

                    boolean pronadjena = false;

                    for (StavkaIznajmljivanja s : stavke) {
                        if (s.getVideoIgra() != null
                                && s.getVideoIgra().getIdVideoIgra() != null
                                && s.getVideoIgra().getIdVideoIgra().equals(vi.getIdVideoIgra())) {
                            pronadjena = true;
                            break;
                        }
                    }

                    if (pronadjena) {
                        filtrirana.add(i);
                    }
                }

                lista = filtrirana;
            }

            ModelTabeleIznajmljivanje model
                    = (ModelTabeleIznajmljivanje) tblIznajmljivanje.getModel();
            model.setLista(lista);

            if (lista.isEmpty()) {
                JOptionPane.showMessageDialog(this,
                        "Sistem ne može da nađe iznajmljivanja po zadatim kriterijumima.");
            } else {
                JOptionPane.showMessageDialog(this,
                        "Sistem je našao iznajmljivanja po zadatim kriterijumima.");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnPretraziIznajmljivanjeActionPerformed


