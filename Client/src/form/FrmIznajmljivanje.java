    private void btnIzmeniIznajmljivanjeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniIznajmljivanjeActionPerformed
        try {
            int selectedRow = tblIznajmljivanje.getSelectedRow();

            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(this, "Sistem ne može da nađe iznajmljivanje.");
                return;
            }

            ModelTabeleIznajmljivanje model = (ModelTabeleIznajmljivanje) tblIznajmljivanje.getModel();
            Iznajmljivanje i = model.vratiIznajmljivanje(selectedRow);

            List<StavkaIznajmljivanja> stavke
                    = Communication.getInstance().ucitajStavkeIznajmljivanja(i);

            i.setStavke(stavke);

            IzmeniIznajmljivanje forma = new IzmeniIznajmljivanje(admin, i);
            forma.setLocationRelativeTo(this);
            forma.setVisible(true);

            ucitajIznajmljivanja();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Greška pri otvaranju forme: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnIzmeniIznajmljivanjeActionPerformed

