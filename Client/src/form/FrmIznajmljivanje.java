    private void srediTabelu() {
        ModelTabeleIznajmljivanje mti = new ModelTabeleIznajmljivanje();
        tblIznajmljivanje.setModel(mti);
    }

    private void ucitajIznajmljivanja() {
        try {
            Response response = Communication.getInstance().ucitajIznajmljivanja();

            if (response.getStatus() == ResponseStatus.ERROR) {
                throw response.getException();
            }

            List<Iznajmljivanje> lista = (List<Iznajmljivanje>) response.getPodatak();

            ModelTabeleIznajmljivanje model = (ModelTabeleIznajmljivanje) tblIznajmljivanje.getModel();
            model.setLista(lista);

            JOptionPane.showMessageDialog(this, "Sistem je učitao iznajmljivanja.");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da učita iznajmljivanja.\n" + ex.getMessage());
        }
    }

    private void pripremiFormu() {
        tblIznajmljivanje.setModel(new ModelTabeleIznajmljivanje());

        ucitajComboOsobe();
        ucitajComboVideoIgre();
        ucitajComboZaposleni();
    }

    private void ucitajComboOsobe() {
        try {
            Response response = Communication.getInstance().ucitajOsobe();

            if (response.getStatus() == ResponseStatus.ERROR) {
                throw response.getException();
            }

            List<Osoba> lista = (List<Osoba>) response.getPodatak();

            cmbOsoba.removeAllItems();

            Osoba sve = new Osoba();
            cmbOsoba.addItem(sve);

            for (Osoba o : lista) {
                cmbOsoba.addItem(o);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Greška pri učitavanju osoba: " + ex.getMessage());
        }
    }

    private void ucitajComboVideoIgre() {
        try {
            Response response = Communication.getInstance().ucitajVideoIgre();

            if (response.getStatus() == ResponseStatus.ERROR) {
                throw response.getException();
            }

            List<VideoIgra> lista = (List<VideoIgra>) response.getPodatak();

            cmbVideoIgra.removeAllItems();

            VideoIgra sve = new VideoIgra();
            cmbVideoIgra.addItem(sve);

            for (VideoIgra vi : lista) {
                cmbVideoIgra.addItem(vi);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Greška pri učitavanju video igara: " + ex.getMessage());
        }
    }

    private void ucitajComboZaposleni() {
        cmbZaposleni.removeAllItems();

        Zaposleni svi = new Zaposleni();
        cmbZaposleni.addItem(svi);

        Zaposleni z1 = new Zaposleni();
        z1.setIdZaposleni(1L);
        z1.setIme("Admin");
        z1.setPrezime("Admin");

        Zaposleni z2 = new Zaposleni();
        z2.setIdZaposleni(2L);
        z2.setIme("Marko");
        z2.setPrezime("Markovic");

        Zaposleni z3 = new Zaposleni();
        z3.setIdZaposleni(3L);
        z3.setIme("Jelena");
        z3.setPrezime("Jovanovic");

        cmbZaposleni.addItem(z1);
        cmbZaposleni.addItem(z2);
        cmbZaposleni.addItem(z3);
    }

