    private void pripremiComboBox() {
        cmbKategorija.removeAllItems();
        cmbKategorija.addItem("Standard");
        cmbKategorija.addItem("Student");
        cmbKategorija.addItem("Premium");
    }

    private void popuniFormu() {
        txtIme.setText(izabrana.getIme());
        txtPrezime.setText(izabrana.getPrezime());
        txtEmail.setText(izabrana.getEmail());
        txtTelefon.setText(izabrana.getTelefon());

        if (izabrana.getKategorijaOsobe() != null) {
            cmbKategorija.setSelectedItem(izabrana.getKategorijaOsobe().getNaziv());
        }
    }

    private void validirajFormu() throws Exception {
        if (txtIme.getText().trim().isEmpty()) {
            throw new Exception("Ime je obavezno.");
        }
        if (txtPrezime.getText().trim().isEmpty()) {
            throw new Exception("Prezime je obavezno.");
        }
        if (txtEmail.getText().trim().isEmpty()) {
            throw new Exception("Email je obavezan.");
        }
        if (txtTelefon.getText().trim().isEmpty()) {
            throw new Exception("Telefon je obavezan.");
        }
        if (cmbKategorija.getSelectedItem() == null) {
            throw new Exception("Kategorija je obavezna.");
        }
    }

    private KategorijaOsobe vratiKategorijuIzComboboxa() throws Exception {
        String naziv = (String) cmbKategorija.getSelectedItem();

        switch (naziv) {
            case "Standard":
                return new KategorijaOsobe(1L, "Standard");
            case "Student":
                return new KategorijaOsobe(2L, "Student");
            case "Premium":
                return new KategorijaOsobe(3L, "Premium");
            default:
                throw new Exception("Nepoznata kategorija.");
        }
    }

