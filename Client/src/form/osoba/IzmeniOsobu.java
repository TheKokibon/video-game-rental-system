    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
         try {
        validirajFormu();

        izabrana.setIme(txtIme.getText().trim());
        izabrana.setPrezime(txtPrezime.getText().trim());
        izabrana.setEmail(txtEmail.getText().trim());
        izabrana.setTelefon(txtTelefon.getText().trim());
        izabrana.setKategorijaOsobe(vratiKategorijuIzComboboxa());

        Response response = Communication.getInstance().promeniOsobu(izabrana);

        if (response.getStatus() == ResponseStatus.SUCCESS) {
            JOptionPane.showMessageDialog(this, "Sistem je zapamtio osobu.");
            this.dispose();
        } else {
            throw response.getException();
        }

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Sistem ne može da zapamti osobu.\n" + ex.getMessage());
    }

    }//GEN-LAST:event_btnIzmeniActionPerformed


